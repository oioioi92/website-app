const PUBLIC = { zh: PUBLIC_ZH, en: PUBLIC_EN, ms: PUBLIC_MS } as const;
const ADMIN = { zh: ADMIN_ZH, en: ADMIN_EN, ms: ADMIN_MS } as const;

/** 合并 public + admin 的扁平 key，key 带命名空间 public.xxx / admin.xxx */
function buildMessages(locale: Locale): TranslationMap {
  const pub = flatten({ public: PUBLIC[locale] });
  const adm = flatten({ admin: ADMIN[locale] });
  return { ...pub, ...adm };
}

const CACHE: Partial<Record<Locale, TranslationMap>> = {};

export function getTranslations(locale: Locale): TranslationMap {
  if (!CACHE[locale]) CACHE[locale] = buildMessages(locale);
  return CACHE[locale]!;
}

export function t(locale: Locale, key: string): string {
  const map = getTranslations(locale);
  return map[key] ?? key;
}
